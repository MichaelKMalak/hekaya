#!/bin/bash
# Random short film idea generator based on the 5 classifications
# Usage: ./idea-generator.sh

EXTREME_CASE=(
    "واحد بيصحى ملوش ذاكرة — بس الناس كلها عارفاه"
    "ست بتلاقي رسالة من نفسها في المستقبل"
    "واحد بيكتشف إن كل صوره القديمة فيها حد مش عارفه"
    "بنت بتسمع أفكار الناس — بس ليوم واحد بس"
    "واحد بيموت وبيصحى في نفس اليوم كل مرة"
)

SPACE_LIMIT=(
    "فيلم كامل في أسانسير — اتنين غرباء"
    "عيلة محبوسة في أوضة واحدة في عزا"
    "واحد في تاكسي والسواق مش عايزه ينزل"
    "اتنين على سطح بناية — واحد عايز ينط والتاني بيحاول يمنعه"
    "مواجهة في مطبخ بلدي ضيق بين حماة وعروسة"
)

TWO_OPPOSITES=(
    "أب ملحد وابنه قرر يبقى شيخ"
    "جارتين: واحدة أرملة سعيدة وواحدة متجوزة تعيسة"
    "ضابط شرطة ومتهم — بس المتهم أذكى"
    "واحد غني فقد كل حاجة وواحد فقير لقى فلوس"
    "توأم — واحد ناجح والتاني فاشل — بيتقابلوا بعد ١٠ سنين"
)

SWITCH_FAMILIAR=(
    "قصة سندريلا بس من وجهة نظر الأخت الشريرة — وهي عندها حق"
    "فيلم رعب من وجهة نظر الشبح"
    "قصة حب من وجهة نظر الجرسون اللي بيسمع كل حاجة"
    "يوم عادي — بس كل حاجة معكوسة. الكبار في المدرسة والعيال في الشغل"
    "مشهد الطلاق المعروف — بس من وجهة نظر العيّل اللي قاعد في الأوضة التانية"
)

TIME_LIMIT=(
    "عنده ٥ دقايق يوصّل رسالة قبل ما القطر يمشي"
    "لازم تقول الحقيقة قبل ما المطر يقف"
    "عنده لحد ما الشمعة تولع عشان يعمل أمنية واحدة"
    "آخر ساعة في الخدمة العسكرية — وبيكتشف حاجة"
    "عدّاد الباركينج ١٠ دقايق — ولازم يخلّص الموضوع"
)

# Pick random from each
echo "=== مولّد أفكار أفلام قصيرة ==="
echo ""

CATEGORIES=("🧨 حالة استثنائية" "🏠 حدود المكان" "⚖️ نقيضين" "🔁 قلب المألوف" "⏱️ عدّاد وقت")
ARRAYS=(EXTREME_CASE SPACE_LIMIT TWO_OPPOSITES SWITCH_FAMILIAR TIME_LIMIT)

for i in 0 1 2 3 4; do
    eval "arr=(\"\${${ARRAYS[$i]}[@]}\")"
    idx=$((RANDOM % ${#arr[@]}))
    echo "${CATEGORIES[$i]}:"
    echo "  ${arr[$idx]}"
    echo ""
done

echo "---"
echo "اختار فكرة أو اخلط اتنين مع بعض."
echo "الأفكار دي نقط بداية — طوّرها وخليها بتاعتك."
